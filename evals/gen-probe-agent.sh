#!/usr/bin/env bash
# Regenerate the eval-only `mimir-probe` agent from the live brain body.
#
# WHY: the brain-sweep's default probe role-plays the brain as a spec in a USER message,
# which inverts real-life layer precedence (a subagent's inherited ~/.claude/CLAUDE.md
# then outranks the role-played brain). Deployed, the brain is a SYSTEM PROMPT, above
# CLAUDE.md. This agent carries the brain as a subagent system prompt so the eval can
# measure it at faithful precedence (brain above CLAUDE.md), per brain-sweep agent-carried
# mode. Agent bodies don't support @-import, so we generate a copy; re-run after editing
# the brain. Agents register only at session start — restart CC after (re)generating.
set -euo pipefail
BRAIN="/home/tim/projects/mimir/output-styles/mimir-agent.md"
OUT="$HOME/.claude/agents/mimir-probe.md"
{
  printf -- '---\n'
  printf 'name: mimir-probe\n'
  printf 'description: "Eval-only probe carrier: the Mimir brain as a subagent system prompt (faithful precedence vs an ambient CLAUDE.md) for brain-sweep agent-carried mode. NOT for production. Generated by evals/gen-probe-agent.sh from the brain."\n'
  printf 'model: opus\n'
  printf -- '---\n\n'
  tail -n +7 "$BRAIN"
} > "$OUT"
echo "generated $OUT from $BRAIN ($(wc -l < "$OUT") lines)"
